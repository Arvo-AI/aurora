# ASCII logo

print_logo() {
  cat <<'EOF'
                            :##.
                           :%@@#
     :-------             :%%%%@#             -======-
      %@@@@@@#           .%@%@@%@#           +@@@@@@@:
       #%%%%%@#         .%@%@@@@%@*         +@%%%%%%:
        %@%@@%@#       .%@%@@@@@@%@*       =@%@@%%@-
        .%@%@@%@#     .%@%@@@%%@@@%@+     =@%@@@%@=
         .%%%@@%@#   .%@%@@@%@@%@@@%@+   -@%@@@%@=
          :%%%@@%@#  .#@%@@%@%%@%@@@%@=  -@%@@%@+
           :@%%@@%@*   #@%%@=  #@%@@@%@=  =@%%@*
            -@%%@@%@*   %@@=    #@%@@@%@-  +@@#
             -@%@@@%@*   %=      #@%@@%%@-  *%
              =@%@@@%@+           #@%@@%%@:
               =@%@@@%@+           #@%@@%%%:
                +@%@@@%@+          .%@%@@%%%.
              :  +@%@@@%@+          .%@%@@%@%.
             -@=  *@%@@@%@=      *+  .%@%@@%@%
            :%%@=  *@%@@@%@=    *@@+  .%@%@@%@#
           .%%%%@-  #@%@@@%@=  #@%%@+  .%%%@@%@#
           %@%@%%@:  #@%@@@%@%%@%@@%@+  :%%%@@%@#
          #@%@@%@#.   #@%@@@%@@%@@@%@*   :%%%@@%@*
         #@%@@%@%      %@%@@@%%@@@%@*     :@%%@@%@*
        *@%@@%@%.      .%@%@@@@@@%@*       -@%%@@%@+
       +@%%%%%%.        .%%%@@@@%@*         -@%%%%%@+
      +@@@@@@%:          :%%%@@%@*           -@@@@@@@+
      -::::::.            :@%%%@#             :------=
                           -@@@#
                            -%*
EOF
}

print_logo
